<p>
Who can tell the story of Medea, the true story? The heroes who knew her are dead or scattered to the winds – too concerned with their own reputations to care about a woman’s fate. The man closest to her was Jason, but he can no longer be trusted.
<p>

</p>
As a young man, Jason dreamed of heroic deeds, of glory – he wanted to reclaim his family’s ancient throne. This desire has driven him here, to this: a wreck, beaten to splinters on a foreign shore. He, who was so promising in his youth.
<p>

</p>
He was raised to be a hero, trained with Chiron on a distant shore, became a master of both sword and lyre. And women – not much is said about the women, but surely he learned to play upon their strings, too. Even I can testify to his power. Otherwise, I would not have been entombed in this prison that echoes my voice.
<p>

</p>
When Jason returned to the land of his father to reclaim the throne, he met Hera, disguised as an old crone begging for help. With his strong arms, Jason carried her across the Rubiconian river, like a blind hero.
<p>

</p>
Pity, he lost a sandal during the deed. A hero with leopard skin across his shoulders, spears in his hands, hair in curls down his back – was he to return home with only one sandal, limping like a beggar?
<p>

</p>
Long he searched the silt, unaware that the gods were playing their game. The die was cast.
<p>

</p>
The King of Iolcos knew that danger would come walking with only one sandal; thus the oracle had foretold. And when Jason arrived, limping yet proud, the king understood that cunning was required.
<p>

</p>
He treated Jason well, welcoming him with a feast. Jason sat opposite the king, like unto a god, and demanded the throne. It was his birthright, his lawful claim, he said.
</p>

<p>
The king replied that the crown weighs heavy on an old man. But first – would the young man not perform a heroic deed, win some glory? All heroes dream of the Golden Fleece of Colchis, he said. Jason’s own father had dreamed of it – the ultimate symbol of royal power. He himself was too old for such a feat. But Jason, so young and handsome, with two spears at his side?
<p>

<h3>II<br>
Vocis meae<br>
<span style="font-weight: regular;">(Av min röst)</span>
</h3>


Fortfarande hade ingen sett musen.

<p>
Kungen nickade åt en grupp musiker som satt i ett hörn och såg uttråkade ut. De ryckte till och började spela på harpa, basfiol och flöjt. Ingen spelade i takt, ty det hände så sällan att de fick spela, men nu tog de skadan igen och blåste på för kung och fosterland.

<p>
Kungen höll en serviett framför munnen så ingen skulle se honom tala. Mitt namn är Kungen, sa kungen, och du är välkommen att dela en enkel måltid med mig.

<p>
Kungen bugade lätt på huvudet. Publiken såg att han bugade sig. De bugade sig också. Och neg. Allt efter sed och tradition.
Tack! sa du’Ellen. Hon satte sig på bordet framför kungen och knöt en serviett runt halsen. Jag är här för att uppfylla en spådom, sa hon.

<p>
Kungen höll tekannan högt så att ingen kunde se honom och frågade: Te? Publiken ställde sig på tå och sträckte sina halsar för att se. Tack, gärna! sa musen.

<p>
Kungen lutade sig åt sidan så han skymdes av en stor fruktskål och sa: En vindruva? Publiken lutade sig åt sidan för att se vad han gjorde, men ingen såg att han gav vindruvor till en mus. Det var riktigt trevligt med sällskap, tyckte kungen.
<p>

<p>
Från och med den dagen åt musen frukost med kungen varje dag. Kungen utökade sin orkester med en trumslagare och en pukblåsare. Då kunde han prata med musen utan att någon hörde något.
</p>

<p>
Han ordnade fram mer mat, mycket mer mat. Snart hade ett helt berg framför sig. Nu såg man bara toppen på hans hatt som vippade när han talade.
</p>

<p>
du’Ellen hade rest vida omkring, upplevt stort och smått och allt däremellan. Hon hade roliga historier att berätta om kungens grannländer.
</p>

<p>
I Trickland var det hovnarren som var kung. Alla adelsmän hade strutar på huvudet och drack löksoppa genom näsan.
I Nordland bodde folket i hus av snö. När sommaren kom smälte husen bort och alla bodde i sina båtar. 
</p>

<p>
Kungen skrattade gott åt detta, men när han frågade om Romland blev du’Ellen märkbart tyst. Det landet ville hon inte tala om.
</p>

<p>
du’Ellen kunde göra konster också, som att spetsa vindruvor på sin värja när kungen kastade upp dem i luften. Eller hoppa genom flottyrringar som han rullade på bordet.
</p>

<p>
Ibland lekte hon ”hav i soppskålen” och seglade på en skorpa. Andra gånger jonglerade hon ärtor eller gick på apelsiner som rullade fram på bordet.
</p>

<p>
Folket pratade om kungens lustiga vanor och visste inte vad de skulle tro. Å ena sidan blev de oroliga och trodde att krig och galenskap var nära. Å andra sidan var kungen gladare än vanligt. Han gick ofta och sjöng för sig själv eller skrattade utan anledning.
</p>

<p>
För att ingen skulle tro att deras kung var konstig började resten av befolkningen också sjunga och skratta utan anledning. Det blev på det hela taget en ganska trevlig tid.
</p>

<p>
En dag tog musen mod till sig och frågade om kungen ville gifta sig med henne. Kungen blev alldeles paff. Gifta sig? Han visste inte vad han skulle säga. Gifta sig med en mus?
</p>

<p>
Det var därför jag reste hit, sa hon. Jag reste över höga berg och djupa skogar, genom sju år av träsk och leda. Jag är prinsessa i ett stort rike, och genom ett giftermål skulle mitt land slås ihop med ditt. Ingen behöver längre frukta krig och grämelse.
</p>

<p>
Nu satt kungen i en knipa. Den lilla musen hade blivit honom kär de sista månaderna, men om han gifte sig med en mus skulle de andra kungarna i länderna runt omkring skratta ut honom. Särskilt fruktade han de vilda romlanderna i norr. De som åt rått kött och kunde förvandlas till djur. 
</p>

<p>
Han var tvungen att tacka nej. du’Ellen slokade med morrhåren och mumlade något om ödets tunga lott innan hon försvann ur kungens liv, lika oväntat som hon dykt upp. 
</p>

<p>
Nu var det tomt vid kungens bord. Ingen mus som berättade roliga historier. Ingen du’Ellen som sjöng roliga visor eller fäktades med persiljekvistar.
</p>

<p>
Varje dag satt kungen dyster vid sitt bord och ville varken äta eller dricka. Han nickade åt orkestern att vara tyst och suckade djupt, medan alla som kommit för att titta på honom höll andan för att inte drabbas av panik. Allt var inte väl med kungen. Allt var inte väl med landet.
</p>

<p>
Vad gör man med en kung som inte äter? Alla kungens ministrar, rådsmän och narrar diskuterade saken utan att komma någon vart. Till slut steg en modig riddare fram till kungen som håglöst satt och viftade med en persiljekvist.
</p>

<p>
Vad skola whi, Folket, göra så att vår konung, ers majestätiska höghet, skola blifva lycklig igen? sa riddaren. När herr Kungen är ledset är Folket också ledset, förklarade hon. Barnen leker inte med sina leksaker, och de vuxna slipar svärden.
</p>

<p>
Ge mig en mus, sa kungen. Jag önskar mig en mus.
</p>

<p>
En mus, sa alla som stod på tå och lyssnade. Var det så enkelt? Ge kungen en mus? Sådana fanns ju lite varstans. En man drog sig till minnes att han sett en mus förra veckan. En annan kom ihåg att han sett två möss natten innan. En tredje sa att han hade en mus i fickan.
</p>

<p>
Men inte vilken mus som helst, sa kungen och alla tystnade tvärt. Hon ska ha kläder och svärd och sjunga och berätta roliga historier. Jag saknar henne, sa han. Jag skulle ge halva mitt kungarike för att få henne tillbaka.
</p>

<p>
I nästa ögonblick var rummet tomt. Alla sprang för att leta efter en mus. Det är inte var dag man har chansen att vinna halva kungariket.
</p>

<p>
Nu följde en orolig men rolig tid då alla letade efter möss, klädde dem i kläder och försökte lära dem konster. Varje dag samlades hoppfulla människor vid kungens frukostbord där de presenterade sina möss i hopp om att vinna halva kungariket med omnejd.
</p>

<p>
Många möss passerade för kungens ögon, men ingen av dem var du’Ellen – och du’Ellen var den enda mus kungen ville ha.
</p>

<p>
Ett rykte spreds sig att kejsaren av Romland hade hittat en mus som gick med svärd, och den kunde både tala och sjunga och göra konster. Precis som kungen önskade.
</p>

<p>
Fast det visste ju alla att romlanderna kunde förvandla sig till djur, så egentligen var det fusk. Men ingen vågade stå i vägen då kejsaren av Romland stegade fram till kungens frukostbord. På bordet ställde han en liten bur, och i buren satt en mus.
</p>

<p>
Här har’u musen du söker! Nu e hon din. Å halva ditt kungarike e mitt, tjöt kejsaren av Romland. Å när dotera mi dyker upp, efter att ha varit försvunnen i träsk och leda i sju tunga år, då ska ho gefta seg med deg och få den andra halvan. För så säger spådomen. Ha! Ho ho! Hi!
</p>

<p>
Ingen i kungens rike hade hört talas om denna förskräckliga spådom, men å andra sidan kunde man förvänta sig vad som helst av de vilda romlanderna som åt rått kött och kunde förvandla sig till djur.
</p>

<p>
Då hände något märkvärdigt. Kungen tog ut den lilla musen ur buren och höll henne mot sitt öra. Ja, det såg ut som om musen viskade något i kungens öra. Han log stort och sa med hög röst:
</p>

<p>
Så får det bli! Jag tar musen, du får halva riket.
</p>

<p>
Oj, oj, oj, en större skandal kunde ingen dra sig till minnes. Det överträffade den gången då kocken nös i soppan eller (man ryser vid minnet) då kungen spillde vin på kragen.
</p>

<p>
Kejsaren av Romland skrattade så det skallrade i hela slottet. Han slog sig på knäna och dansade en dans och skrattade igen. Så här roligt hade han inte haft sedan hans fru fjärtade i bastun.
</p>

<p>
Och när kungen sa att han ämnade gifta sig med musen, och det redan samma kväll, då skrattade kejsaren av Romland så mycket att han fick hjärtslag och dog på fläcken. 
</p>

<p>
Se, på den tiden visste man hur man roar sig.
</p>

<p>
Kungen ställde till med stor fest redan samma kväll. Fort! Kungen har bråttom. Han ska gifta sig med en mus.
</p>

<p>
Bröllopet hölls i kungens frukostmatsal. Det var lika bra, för där stod ju redan maten framdukad. Det var fullt med folk från golv till tak. Och de som inte fick plats i rummet hängde i fönstret eller klättrade ner genom skorstenen. 
</p>

<p>
Kungen skulle gifta sig!
</p>

<p>
Där kom han, fortfarande klädd i sin morgonrock. Och där var musen. Kungens skräddare hade sytt en brudklänning av kungens näsduk, och svärdet stack ut bredvid svansen. 
</p>

<p>
Prästen kom och mumlade en massa på latin som ingen förstod, och så var det bara kyssen kvar.
</p>

<p>
Kungen lyfte upp musen. Alla höll andan. Kungen lutade sig över musen. Någon snyftade till av rörelse. Kungen kysste musen.
</p>

<p>
Poff!
</p>

<p>
En hög smäll hördes. Kungen och musen försvann i ett moln av rök. 
</p>

<p>
Elden är lös! skrek någon och alla försökte rusa ut. Kvar stod bara kungen och – ja, vem stod vid hans sida? Musen? Nej, en ung kvinna.
</p>

<p>
De höll händer och såg in i varandras ögon. De märkte inte smällen eller rökmolnet eller det allmänna tumultet. Kungen och hans nya drottning hade bara ögon för varandra.
</p>

<p>
Men vem var hon?
</p>

<p>
Svart hår, mörka ögon. Hon hade något vilt och otämjt över sig. Nu såg alla vem det var. Det var prinsessan av Romland. Hon var tillbaka efter sju år i träsk och leda. Och hon hade gift sig med kungen! Och fått halva hans kungarike, precis som spådomen hade förutspått.
</p>

<p>
Nu kunde man pusta ut. Allt är väl med kungen. Allt är väl i landet. 
</p>


