<h2>Efterord</h2>

<p>Att återbruka klassiker till moderna sagor är ett sätt att skala av kulturella avlagringar som talade till den samtida publiken men inte berör oss lika mycket.</p>

<p>Detta framkommer extra tydligt i Sagan om Fettpärlan.</p>

<p>I originalet är nio passagarerar på flykt undan krigets vedermödor. Varje person representerar en viss samhällsgrupp, moralisk hållning eller ideologi från 1870-talets frankrike. Denna aspekt av originaltexten är nedtonad i Sagan om Fettpärlan.</p>

<p>Det gör att kärnan framkommer tydligare. Den som står lägst på samhällsstegen visar sig vara den som bäst uppfyller tidens ideal om patriotism och självuppoffring. Lägg till mod och hjärta så har du Fettpärlan i ett nötskal.</p>

<p>Men när hon själv behöver hjälp så vänder gruppen henne ryggen. Hon sätts på plats, faller tillbaka till sin plats i hierarkin. Ordningen är återställd.</p>

<p>När jag bad en vän läsa min saga och berättade om handlingen sköt hon pappren i från sig och sa: ”Varför ska jag läsa en bok om en våldtäkt?” För att det förekommer sa jag.</p>

<p>Onda ting händer i verkligheten, därför finns de i litteraturen också. Det är så vi kan möta dilemman, sätta oss in i andra människors psyken, ställas inför vår egna moraliska kompass.</p>

<p>Vi har alla en Fettpärla inom oss – och bredvis oss.</p>


