<p>Fettpärlan kom springande med pälsen på ända och hoppade upp i vagnen.</p>

<p>Om jag inte vetat bättre skulle jag tro att ni tänkte åka utan mig! sa hon.
Hundarna svarade inte, utan drog sig undan. Fettpärlan luktade snus, krut och surkål. Hon luktade människa!</p>

<p>Fettpärlan kände också att hon luktade och började slicka sig ren, men hur hon än försökte, så vägrade lukten att försvinna.</p>

<p>Otäcka tider vi lever i, sa Mr Bulldog utan att vända sig till någon särskild.</p>

<p>Det är tur att vi är på väg mot anständiga trakter, sa Madame de Papillon med en blick åt Fettpärlan.</p>

<p>Ja, man får stå ut med mycket, sa Amiral Schnauzer och slickade sina mustascher.</p>

<p>Efter en stund blev hundarna hungriga. De började plocka med mat i olika korgar, tvekade, tog en bit, stoppade tillbaka, åt lite med frånvarande min, insåg att de var hungriga, tog lite mer.</p>

<p>Fettpärlan hade arbetat hela natten och hade inte hunnit göra en matsäck den här gången. Hennes mage kurrade, men ingen verkade märka det.</p>

<p>Tänka sig. Här satt hon. En katt i sina bästa år. Som sköter sina affärer. Utkämpar sina strider. Räddar hundar. Och vad får hon för det? Ingenting!</p>

<p>Fettpärlan bestämde sig. Hon skulle leta upp sina ungar. De hade tagits till en ”bättre plats”. Hon visste inte var, men det var en gård på landet. Med träd att klättra i. Och massor av frisk luft. Hon skulle hitta dem och berätta att de hade en mor som älskade dem, som alltid älskat dem – och alltid skulle älska dem.</p>

<p>Medan hundarna åt började Fettpärlan gråta, en stilla, inåtvänd gråt som inte rörde någon annan än henne själv.</p>

<p>Amiral Schnauzer slickade sina mustascher och log. Under sin grova päls var han något av en rebell, och han började nynna på en sång som blivit populär under kriget, en sång om mod, jämlikhet och kamp.</p>

<p>”Hjulen på vagnen går runt runt runt<br>
runt runt runt<br>
runt runt runt.<br>
Hjulen på vagnen går runt runt runt<br>
hela dagen lång…”</p>

<p>Att höra Amiral Schnauzer nynna denna sång gjorde hundarna irriterade och nervösa.</p>

<p>”Hunden viftar svans svans svans<br>
svans svans svans<br>
svans svans svans<br>
Hunden viftar svans svans svans<br>
hela natten lång…”</p>

<p>Vad ville han med det? Vad antydde han? Om han inte slutade snart skulle de börja yla.</p>

<p>”Kriget, det pågår överallt<br>
överallt<br>
överallt<br>
Kriget, det pågår överallt<br>
hela dagen lång…”</p>

<p>Det hördes ljud av gråt från Fettpärlans hörn. Och sången, den fortsatte utan nåd, timme efter timme, medan sällskapet reste vidare, under en stigande sol, som inte skänkte någon värme.</p>

