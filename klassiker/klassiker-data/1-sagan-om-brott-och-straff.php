<h2>Efterord</h2>

<p>Det finns vanliga människor som lydigt följer lagar och regler – och så finns det extraordinära människor, Napoleons like, som har rätt att begå brott om det gynnar mänskligheten.</p>

<p>I denna febriga tankevärld simmar Raskolnikov omkring, en ung man som försöker leva upp till en teori som hans psyke inte klarar av.</p>

<p>I början av romanen Brott och straff dödar han pantlånerskan och hennes syster, men han får inte mycket glädje av sitt dåd. Vare sig pengar eller sinnesro får han. Till slut erkänner han brottet för polisen och accepterar att skickas på straffarbete i Sibirien.</p>

<p>Brott och straff är en av de mest lästa romanerna i världen och en av de mest analyserade. Den kom ut som följetång i tidningen Russkij Vestnik år 1866. Det är en tid då Fjodor Dostovjeskijs Ryssland kokar av nya idéer: om nihilism, om utilitarism, om radikal rationalism och övermänniskan.</p>

<p>Dostojevskij hade själv dömts till döden, benådats i sista sekund och suttit i straffläger i fyra år. Han visste vad det innebar att förlora sin frihet, tvingas omvärdera sitt liv och möta en religiös omvälvning. Detta kom att påverka både hans liv och hans böcker, som säger att lidande kan leda till insikt.</p>

<p>I romanen jobbar inte Sonja hos pantlånerskan, utan vi får se hur hon uppoffrar sig och säljer sin kropp för att försörja sin far och hans nya familj.</p>

<p>Ju mer hon offrar sig för sin far, desto mer usel känner han sig, vilket driver honom ännu djupare in i alkoholmissbruk. Vi ser samma dynamik med Raskolnikov och hans syster. Han är den som ska försörja familjen; hon ska inte behöva offra sig för hans skull.</p>

<p>Kvinnorna blir en spegel som visar männen hur långt de är ifrån sin idealbild. Marmeladov kan inte ändra sig och går under. Raskolnikov klarar att se sig själv som den han är i sista stund – och påbörjar den långa vandringen mot förändring.</p>

<p>Därför lever han än.</p>

