<h2>Efterord</h2>

<p>De äldsta bevarade texterna om Gilgamesh är 4 100 år gamla och bygger på äldre muntliga traditioner och (delvis) verkliga händelser. Under berättelsens gång utvecklas Gilgamesh från självisk ung härskare till en äventyrare (hjälte) som slåss mot monster, för att sedan bli en existentiell grubblare och slutligen en vis man och mogen härskare. 
</p>
<p>Men berättelsen om Gilgamesh handlar även om civilisationen kontra naturen. Gilgamesh representerar civilisationen, staden, han är stolt över Uruks starka murar. Enkidu representerar naturen. Han lever i nuet, i samklang med det vilda, och har inget begrepp om döden. 
</p>
<p>Gilgamesh och Enkidu dödar Humbaba för att skaffa cederträ, för att bevisa sin styrka och vinna ära. De visar att människan kan erövra naturen – civilisationens seger över vildmarken.
</p>
<p>Men det är också ett oerhört brott mot både gudarnas lagar och naturens ordning. När de dessutom dödar Himmelstjuren har de gått för långt. Gudarna (naturen) slår tillbaka. Eftersom Gilgamesh är delvis gud blir det Enkidu som får dö.
</p>
<p>Enkidu var naturbarnet som blev människa genom kulturens gåvor: bröd och öl, kläder och olja. Men genom att bli människa lämnar han sitt paradis, sitt eden, och blir en dödlig varelse.
</p>
<p>När han vet att underjorden väntar och är bitter över sitt öde talas han tillrätta av Samas, solens gud. Samas säger att civilisationen gjorde Enkidu vek, men den gav honom också vänskap, kärlek, språk och mening. Hans liv blev kortare, men rikare.
</p>
<p>Enkidu lämnar jorden i frid, men Gilgamesh har lång väg kvar att gå innan han kan försonas med sitt öde. 
</p>
<p>Gilgamesh är mer gud än människa. Det är hans dilemma. Han är för mäktig för att vara en vanlig människa – och för mänsklig för att undkomma döden.
</p>
<p>Först när han dyker i Apsu uppnår han viset. Han inser att människor kan leva vidare genom det arbete de gör, som att bygga stora städer med kraftiga murar.
</p>
<p>Mycket av tematiken i Gilgamesh återkommer i senare verk. Vi ser den i den grekiska mytologin. Vi ser den i bibelns mytologi. Även moderna filmer och spel hämtar kraft ur den mesopotamiska myllan. Det var där civilisationen föddes. De gav oss öl och bröd. De gav oss gudar och hjältar. De gav oss sagan. Vi vandrar alla i Gilgamesh skugga. 
</p>

